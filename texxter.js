let box_size = 0.3
let letter_size = 6 * box_size

function draw_letter(letter, letter_number, t, position, color) {
    let x = position[0]
    let y = position[1]
    let z = position[2]
    for (var i = 0; i < letter.length; i++) {
        var line = letter[i];
        for (var j = 0; j < line.length; j++) {
            if (line[j] == 1) {
                walls.push({
                    "_time": t,
                    "_lineIndex": 0,
                    "_type": 1,
                    "_duration": 26,
                    "_width": 0,
                    "_customData": {
                        _track: "brick",
                        _color: color,
                        _fake: true,
                        _interactable: false,
                        _scale: [box_size, box_size, 0.5],
                        _position: [j * box_size + letter_size * letter_number, -i * box_size, 0],
                        _animation: {
                        // here we make the animation for how the cubes are gonna be assembled into letters
                            _definitePosition: [[Random(-15, 15), Random(10, 20), Random(0, 0), 0], [x, y, z, 0.1], [x, y, z, 0.7], [Random(-15, 15), Random(10, 20), Random(0, 0), 1]],
                            _dissolve: [[0, 0], [1, 0.1], [0, 0.999]]

                        }
                    }
                })
            }
        }
    }

}

function draw_sentence(text, position, time, color) {
    let text_size = letter_size * text.length
    position[0] -= text_size / 2
    for (let i = 0; i < text.length; i++) {
        let x = _paint[text[i]];
        draw_letter(x, i, time, position, color)
    }
}

